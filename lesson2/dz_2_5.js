/*
5. Реализовать четыре основные арифметические операции в виде функций с двумя параметрами. 
Обязательно использовать оператор return
*/

function additionNumber(num1, num2) {
	num1 = Number(num1);
	num2 = Number(num2);
	return num1 + num2;
}

function subtractionNumber(num1, num2) {
	return num1 - num2;
}

function multiplicationNumber(num1, num2) {
	return num1 * num2;
}

function divisionNumber(num1, num2) {
	return num1 / num2;
}
